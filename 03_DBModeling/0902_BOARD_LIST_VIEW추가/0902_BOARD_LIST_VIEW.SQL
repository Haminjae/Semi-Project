--VIEW 생성하기 BOARD_LIST

-- BOARD_NUM을 내림차순 순으로 공지사항게시판 조회
SELECT ROWNUM, BOARD_NUM, WRITER_EMAIL, BOARD_TITLE, BOARD_CONTENT, BOARD_IMG, BOARD_DATE, BOARD_DELETE_STATUS, BOARD_WRITER
FROM ( SELECT * FROM BOARD WHERE BOARD_DELETE_STATUS='N' ORDER BY BOARD_NUM DESC)
WHERE ROWNUM BETWEEN 1 AND 10;


-- BOARD_LIST 생성하기
-- 뷰 생성목적: 공지사항 게시판 페이지당 10개만 보여주기위해서
CREATE OR REPLACE VIEW BOARD_LIST
AS
SELECT ROWNUM, BOARD_NUM, WRITER_EMAIL, BOARD_TITLE, BOARD_CONTENT, BOARD_IMG, BOARD_DATE, BOARD_DELETE_STATUS, BOARD_WRITER
FROM ( SELECT * FROM BOARD WHERE BOARD_DELETE_STATUS='N' ORDER BY BOARD_NUM DESC);

